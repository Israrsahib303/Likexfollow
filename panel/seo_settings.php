    // --- LIVE PREVIEW ENGINE ---
    function openSeoModal(id, name, slug, title, desc, keywords) {
        document.getElementById('modalTitle').innerText = 'Optimizing: ' + name;
        document.getElementById('m_page_id').value = id;
        document.getElementById('m_seo_title').value = title;
        document.getElementById('m_seo_desc').value = desc;
        document.getElementById('m_seo_keywords').value = keywords;
        
        let path = slug === 'index' ? '' : slug + '.php';
        document.getElementById('p_url').innerText = 'https://likexfollow.com/' + path;

        updatePreview();
        
        const modal = document.getElementById('seoModal');
        modal.style.display = 'flex';
        setTimeout(() => modal.classList.add('show'), 10);
    }

    function closeSeoModal() {
        const modal = document.getElementById('seoModal');
        modal.classList.remove('show');
        setTimeout(() => modal.style.display = 'none', 300);
    }

    function updatePreview() {
        const titleInput = document.getElementById('m_seo_title').value;
        const descInput = document.getElementById('m_seo_desc').value;
        
        // Update lengths
        const tLen = titleInput.length;
        const dLen = descInput.length;
        
        const tCount = document.getElementById('count_title');
        tCount.innerText = tLen + '/60';
        tCount.style.color = (tLen > 60 || tLen < 30) ? '#ef4444' : '#10b981';

        const dCount = document.getElementById('count_desc');
        dCount.innerText = dLen + '/160';
        dCount.style.color = (dLen > 160 || dLen < 70) ? '#ef4444' : '#10b981';

        // Update Simulator
        document.getElementById('p_title').innerText = titleInput || 'Your Meta Title Goes Here';
        
        // Truncate desc for simulation
        let simDesc = descInput || 'Your meta description will appear here...';
        if(simDesc.length > 155) simDesc = simDesc.substring(0, 155) + '...';
        document.getElementById('p_desc').innerText = simDesc;
    }

    // --- REAL-TIME RANK SCANNER ---
    function scanRanking(slug, btnElement) {
        let originalHTML = btnElement.innerHTML;
        btnElement.innerHTML = '<i class="fas fa-spinner fa-spin"></i> Scanning...';
        btnElement.style.pointerEvents = 'none';

        const formData = new FormData();
        formData.append('ajax_action', 'scan_rankings');
        formData.append('slug', slug);

        fetch(window.location.href, { method: 'POST', body: formData })
        .then(res => res.json())
        .then(res => {
            btnElement.innerHTML = originalHTML;
            btnElement.style.pointerEvents = 'auto';

            if(res.success) {
                if(res.data.length > 0) {
                    let htmlTable = `<div style="text-align: left;"><p class="small text-muted mb-3">Checking URL: <b>${res.url}</b></p><table class="table table-bordered table-sm" style="font-size:14px;">
                        <thead class="table-light"><tr><th>Keyword</th><th>Position</th><th>Vol</th></tr></thead><tbody>`;
                    
                    res.data.forEach(kw => {
                        let posColor = kw.position <= 10 ? 'color: #10b981; font-weight: bold;' : '';
                        htmlTable += `<tr>
                            <td>${kw.keyword}</td>
                            <td style="${posColor}">#${kw.position}</td>
                            <td>${kw.volume}</td>
                        </tr>`;
                    });
                    htmlTable += `</tbody></table></div>`;

                    Swal.fire({
                        title: 'Live Ranking Data 🚀',
                        html: htmlTable,
                        width: '600px',
                        confirmButtonText: 'Awesome',
                        confirmButtonColor: '#6366f1'
                    });
                } else {
                    Swal.fire({
                        icon: 'info',
                        title: 'Not in Top 100 yet',
                        text: `The URL (${res.url}) is not currently ranking in the top 100 on Google US. Keep building backlinks and fresh content!`,
                        confirmButtonColor: '#6366f1'
                    });
                }
            } else {
                Swal.fire({ icon: 'error', title: 'API Error', text: res.error || 'Could not fetch data.' });
            }
        })
        .catch(err => {
            btnElement.innerHTML = originalHTML;
            btnElement.style.pointerEvents = 'auto';
            Swal.fire({ icon: 'error', title: 'Network Error', text: 'Failed to connect to API.' });
        });
    }
</script>

