    $full_url = "likexfollow.com" . ($slug === 'index' ? '/' : '/' . $slug . '.php');
    
    // SEMrush URL Organic API
    $api_url = "https://api.semrush.com/?type=url_organic&key=" . urlencode($api_key) . "&url=" . urlencode($full_url) . "&export_columns=Ph,Po,Nq&database=us&display_limit=15";
    
    $ch = curl_init();
    curl_setopt($ch, CURLOPT_URL, $api_url);
    curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
    curl_setopt($ch, CURLOPT_TIMEOUT, 20);
    curl_setopt($ch, CURLOPT_SSL_VERIFYPEER, false);
    $response = curl_exec($ch);
    $curl_err = curl_error($ch);
    curl_close($ch);

    if ($curl_err) {
        echo json_encode(['success' => false, 'error' => 'cURL Error: ' . $curl_err]);
        exit;
    }

    $rankings = [];
    if ($response && strpos($response, 'ERROR') !== 0 && !empty(trim($response))) {
        $lines = explode("\n", trim($response));
        array_shift($lines); // Remove header
        
        foreach($lines as $line) {
            if (empty(trim($line))) continue;
            $data = str_getcsv($line, ";");
            if (count($data) >= 3) {
                $rankings[] = [
                    'keyword' => $data[0],
                    'position' => (int)$data[1],
                    'volume' => (int)$data[2]
                ];
            }
        }
        echo json_encode(['success' => true, 'data' => $rankings, 'url' => $full_url]);
    } else {
        echo json_encode(['success' => true, 'data' => [], 'url' => $full_url]); // No rankings found
    }
    exit;
}

